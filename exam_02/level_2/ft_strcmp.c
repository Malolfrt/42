int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i]) 
	{
		i++;
	}
	return (s1[1] - s2[i]);
}

#include <stdio.h>

int	main(void)
{
	char	*s10 = "1535";
	char	*s20 = "153a";
	int		res;

	res = ft_strcmp(s10, s20);
	printf("%d\n", res);
	return (0);
}